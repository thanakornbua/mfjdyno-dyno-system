    /**
     * Generates a single-run PDF report.
     *
     * @param detail     run metadata (peaks, config)
     * @param frames     full frame series (for ambient conditions and chart data)
     * @param outputFile destination .pdf path
     */
    public static void writeSingleRun(
        RunHistoryDetailDto detail,
        List<RunHistoryFrameDto> frames,
        Path outputFile
    ) throws IOException {
        PdfFont font = FontProvider.loadSarabunFont();
        PdfWriter writer = new PdfWriter(outputFile.toFile());
        PdfDocument pdfDoc = new PdfDocument(writer);
        Document doc = openDoc(pdfDoc, font);

        // Title
        addHeader(doc, LBL_SINGLE_TITLE, runLabel(detail), font);

        // Metadata + peaks
        doc.add(sectionTitle(LBL_RUN_SUMMARY, font));
        doc.add(buildMetadataTable(detail, font));
        doc.add(vSpacer(6));
        doc.add(buildPeakTable(detail, font));
        doc.add(vSpacer(6));

        // Ambient conditions from peak-power frame
        RunHistoryFrameDto peakFrame = findPeakFrame(frames, detail.getPeakPowerHp());
        if (peakFrame != null) {
            doc.add(buildAmbientTable(peakFrame, font));
            doc.add(vSpacer(6));
        }

        // Chart
        List<ChartSeriesModel> series = makeSingleRunSeries(detail.getRunId(), frames, 0);
        doc.add(sectionTitle(LBL_CHART, font));
        doc.add(makeChartImage(pdfDoc, series, contentWidth(), 255f, font));
        doc.add(vSpacer(12));

        // Footer
        addFooter(doc, font);
        doc.close();
    }

    /**
     * Generates a live-snapshot PDF from the current chart series (no stored run required).
     *
     * @param runLabel   display label for the report title
     * @param caption    optional subtitle (chart caption)
     * @param series     chart series from the live chart model
     * @param outputFile destination .pdf path
     */
    public static void writeLiveSnapshot(
        String runLabel,
        String caption,
        List<ChartSeriesModel> series,
        Path outputFile
    ) throws IOException {
        PdfFont font = FontProvider.loadSarabunFont();
        PdfWriter writer = new PdfWriter(outputFile.toFile());
        PdfDocument pdfDoc = new PdfDocument(writer);
        Document doc = openDoc(pdfDoc, font);

        String subtitle = (caption != null && !caption.trim().isEmpty())
            ? caption.trim()
            : "Live chart snapshot";
        addHeader(doc, LBL_SINGLE_TITLE,
            (runLabel != null && !runLabel.trim().isEmpty() ? runLabel.trim() : "LIVE") + " — " + subtitle,
            font);

        doc.add(sectionTitle(LBL_CHART, font));
        List<ChartSeriesModel> safeSeries = series != null ? series : Collections.<ChartSeriesModel>emptyList();
        doc.add(makeChartImage(pdfDoc, safeSeries, contentWidth(), 255f, font));
        doc.add(vSpacer(12));

        addFooter(doc, font);
        doc.close();
    }

    /**
     * Generates a comparison PDF report for 1–4 runs.
     *
     * @param response   compare response from backend (run detail + frames per run)
     * @param outputFile destination .pdf path
     */
    public static void writeCompare(
        CompareRunsResponseDto response,
        Path outputFile
    ) throws IOException {
        List<ComparedRunDto> runs = runsFrom(response);

        PdfFont font = FontProvider.loadSarabunFont();
        PdfWriter writer = new PdfWriter(outputFile.toFile());
        PdfDocument pdfDoc = new PdfDocument(writer);
        Document doc = openDoc(pdfDoc, font);

        // Title
        String subtitle = "Comparing " + runs.size() + " stored run" + (runs.size() == 1 ? "" : "s");
        addHeader(doc, LBL_COMPARE_TITLE, subtitle, font);

        // Comparison table
        doc.add(sectionTitle(LBL_COMPARE_TABLE, font));
        doc.add(buildCompareTable(runs, font));
        doc.add(vSpacer(12));

        // Chart with all run series overlaid
        List<ChartSeriesModel> allSeries = makeCompareSeries(runs);
        doc.add(sectionTitle(LBL_CHART, font));
        doc.add(makeChartImage(pdfDoc, allSeries, contentWidth(), 255f, font));
        doc.add(vSpacer(12));

        // Per-run metadata blocks
        for (int i = 0; i < runs.size(); i++) {
            ComparedRunDto cr = runs.get(i);
            if (cr.getRun() == null) {
                continue;
            }
            doc.add(sectionTitle(runLabel(cr.getRun()), font));
            doc.add(buildMetadataTable(cr.getRun(), font));
            doc.add(vSpacer(4));
            doc.add(buildPeakTable(cr.getRun(), font));
            doc.add(vSpacer(8));
        }

        addFooter(doc, font);
        doc.close();
    }

    // =========================================================================
    // Document setup helpers
    // =========================================================================

    private static Document openDoc(PdfDocument pdfDoc, PdfFont font) {
        Document doc = new Document(pdfDoc, PageSize.A4);
        doc.setMargins(48, 48, 48, 48);
        doc.setFont(font);
        doc.setFontSize(10);
        return doc;
    }

    /** A4 content width = 595 - 2*48 = 499 pt */
    private static float contentWidth() {
        return 499f;
    }

    // =========================================================================
    // Header / footer
    // =========================================================================

    private static void addHeader(
        Document doc,
        String title,
        String subtitle,
        PdfFont font
    ) {
        doc.add(new Paragraph(title)
            .setFont(font).setFontSize(17).setBold()
            .setFontColor(COL_TXT_MAIN)
            .setMarginBottom(4));

        if (subtitle != null && !subtitle.isEmpty()) {
            doc.add(new Paragraph(subtitle)
                .setFont(font).setFontSize(11)
                .setFontColor(COL_ACCENT)
                .setMarginBottom(2));
        }

        doc.add(new Paragraph(LBL_FOOTER + "  •  " + now())
            .setFont(font).setFontSize(8)
            .setFontColor(COL_TXT_MUTED)
            .setMarginBottom(10));

        // Accent divider
        doc.add(accentDivider(font));
    }

    private static void addFooter(Document doc, PdfFont font) {
        doc.add(new Table(UnitValue.createPercentArray(new float[]{1}))
            .setWidth(UnitValue.createPercentValue(100))
            .setBorder(Border.NO_BORDER)
            .setMarginTop(8)
            .addCell(new Cell()
                .setBorder(Border.NO_BORDER)
                .setBorderTop(new SolidBorder(COL_BORDER, 0.5f))
                .setPadding(4)
                .add(new Paragraph(LBL_FOOTER)
                    .setFont(font).setFontSize(8)
                    .setFontColor(COL_TXT_MUTED)
                    .setTextAlignment(TextAlignment.CENTER))));
    }

    private static Table accentDivider(PdfFont font) {
        return new Table(UnitValue.createPercentArray(new float[]{1}))
            .setWidth(UnitValue.createPercentValue(100))
            .setBorder(Border.NO_BORDER)
            .setMarginBottom(12)
            .addCell(new Cell()
                .setBorder(Border.NO_BORDER)
                .setBorderBottom(new SolidBorder(COL_ACCENT, 2f))
                .setPadding(0)
                .add(new Paragraph("").setFontSize(1)));
    }


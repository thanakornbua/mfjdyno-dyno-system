    private static Table buildMetadataTable(RunHistoryDetailDto d, PdfFont font) {
        Table t = twoCol();
        row(t, LBL_RUN_ID, runLabel(d), font);
        row(t, LBL_DATE, safe(d.getDate()), font);
        row(t, LBL_SOURCE_MODE, safe(d.getSourceMode()), font);
        row(t, LBL_CORRECTION, safe(d.getCorrectionMode()), font);
        return t;
    }

    private static Table buildPeakTable(RunHistoryDetailDto d, PdfFont font) {
        Table t = twoCol();
        row(t, LBL_PEAK_POWER,
            fmtN(d.getPeakPowerHp(), "HP") + "  @  " + fmtN(d.getPeakPowerRpm(), "RPM"), font);
        row(t, LBL_PEAK_TORQUE,
            fmtN(d.getPeakTorqueNm(), "Nm") + "  @  " + fmtN(d.getPeakTorqueRpm(), "RPM"), font);
        return t;
    }

    private static Table buildAmbientTable(RunHistoryFrameDto f, PdfFont font) {
        Table t = twoCol();
        row(t, LBL_AMBIENT_TEMP, fmtN(f.getAmbientTempC(), "°C"), font);
        row(t, LBL_HUMIDITY, fmtN(f.getHumidityPct(), "%"), font);
        row(t, LBL_PRESSURE, fmtN(f.getPressureHpa(), "hPa"), font);
        row(t, LBL_CORR_FACTOR, fmtFull(f.getCorrectionFactor()), font);
        return t;
    }

    private static Table buildCompareTable(List<ComparedRunDto> runs, PdfFont font) {
        int cols = runs.size() + 1;
        float[] widths = new float[cols];
        widths[0] = 2.2f;
        for (int i = 1; i < cols; i++) {
            widths[i] = 1.0f;
        }
        Table t = new Table(UnitValue.createPercentArray(widths))
            .setWidth(UnitValue.createPercentValue(100));

        // Header row
        hdrCell(t, LBL_CMP_METRIC, font);
        for (int i = 0; i < runs.size(); i++) {
            hdrCell(t, runLabel(runs.get(i).getRun()), font);
        }

        // Data rows (lambda-free, compatible with Java 8 source / Java 21 target)
        cmpRow(t, LBL_CMP_POWER, runs, font, new CmpFn() {
            public String get(ComparedRunDto r) {
                return r.getRun() == null ? "—" : fmtN(r.getRun().getPeakPowerHp(), "HP");
            }
        });
        cmpRow(t, LBL_CMP_PWR_RPM, runs, font, new CmpFn() {
            public String get(ComparedRunDto r) {
                return r.getRun() == null ? "—" : fmtN(r.getRun().getPeakPowerRpm(), "RPM");
            }
        });
        cmpRow(t, LBL_CMP_TORQUE, runs, font, new CmpFn() {
            public String get(ComparedRunDto r) {
                return r.getRun() == null ? "—" : fmtN(r.getRun().getPeakTorqueNm(), "Nm");
            }
        });
        cmpRow(t, LBL_CMP_TQ_RPM, runs, font, new CmpFn() {
            public String get(ComparedRunDto r) {
                return r.getRun() == null ? "—" : fmtN(r.getRun().getPeakTorqueRpm(), "RPM");
            }
        });
        cmpRow(t, LBL_CMP_SPEED, runs, font, new CmpFn() {
            public String get(ComparedRunDto r) {
                RunHistoryFrameDto pk = CompareDisplayMapper.peakPowerFrame(r);
                return fmtN(CompareDisplayMapper.frameValue(pk, CompareDisplayMapper.Metric.SPEED), "km/h");
            }
        });
        cmpRow(t, LBL_CMP_AFR, runs, font, new CmpFn() {
            public String get(ComparedRunDto r) {
                RunHistoryFrameDto pk = CompareDisplayMapper.peakPowerFrame(r);
                return fmtN(CompareDisplayMapper.frameValue(pk, CompareDisplayMapper.Metric.AFR), "");
            }
        });
        cmpRow(t, LBL_CMP_AMBIENT, runs, font, new CmpFn() {
            public String get(ComparedRunDto r) {
                return CompareDisplayMapper.ambientText(CompareDisplayMapper.peakPowerFrame(r));
            }
        });
        return t;
    }

    // =========================================================================
    // Chart: PdfFormXObject built from series data
    // =========================================================================

    private static Image makeChartImage(
        PdfDocument pdfDoc,
        List<ChartSeriesModel> series,
        float w,
        float h,
        PdfFont font
    ) {
        PdfFormXObject xObj = new PdfFormXObject(new Rectangle(0, 0, w, h));
        PdfCanvas cv = new PdfCanvas(xObj, pdfDoc);
        drawChart(cv, w, h, series, font);
        cv.release();
        Image img = new Image(xObj);
        img.setWidth(w);
        img.setHorizontalAlignment(HorizontalAlignment.LEFT);
        return img;
    }

    private static void drawChart(
        PdfCanvas cv,
        float totalW,
        float totalH,
        List<ChartSeriesModel> series,
        PdfFont font
    ) {
        // Plot area (inside the margins allocated for axis labels)
        float px = CHART_ML;
        float py = CHART_MB;
        float pw = totalW - CHART_ML - CHART_MR;
        float ph = totalH - CHART_MB - CHART_MT;

        // Compute data ranges across all series
        double maxX = 1000;
        double maxY = 100;
        boolean hasData = false;
        for (ChartSeriesModel s : series) {
            for (ChartPlotPoint p : s.getPoints()) {
                if (p.getX() > maxX) maxX = p.getX();
                if (p.getY() > maxY) maxY = p.getY();
                hasData = true;
            }
        }
        maxX = niceMax(maxX, 1000);
        maxY = niceMax(maxY, 50);

        // Plot background
        cv.saveState();
        cv.setFillColor(CHART_BG);
        cv.rectangle(px, py, pw, ph);
        cv.fill();
        cv.restoreState();

        // Grid lines
        cv.saveState();
        cv.setStrokeColor(CHART_GRID);
        cv.setLineWidth(0.4f);
        int xDiv = 8;
        int yDiv = 6;
        for (int i = 1; i < xDiv; i++) {
            float gx = px + pw * i / (float) xDiv;
            cv.moveTo(gx, py).lineTo(gx, py + ph).stroke();
        }
        for (int i = 1; i < yDiv; i++) {
            float gy = py + ph * i / (float) yDiv;
            cv.moveTo(px, gy).lineTo(px + pw, gy).stroke();
        }
        cv.restoreState();

        // Plot border
        cv.saveState();
        cv.setStrokeColor(CHART_BORDER);
        cv.setLineWidth(0.8f);
        cv.rectangle(px, py, pw, ph);
        cv.stroke();
        cv.restoreState();

        // Series lines
        for (ChartSeriesModel s : series) {
            List<ChartPlotPoint> pts = s.getPoints();
            if (pts.isEmpty()) continue;
            cv.saveState();
            cv.setStrokeColor(hexRgb(s.getColorHex()));
            cv.setLineWidth(1.6f);
            boolean first = true;
            for (int i = 0; i < pts.size(); i++) {
                ChartPlotPoint p = pts.get(i);
                float cx = clamp(px + (float) (p.getX() / maxX) * pw, px, px + pw);
                float cy = clamp(py + (float) (p.getY() / maxY) * ph, py, py + ph);
                if (first) { cv.moveTo(cx, cy); first = false; }
                else        { cv.lineTo(cx, cy); }
            }
            if (!first) cv.stroke();
            cv.restoreState();
        }

        // No-data label
        if (!hasData) {
            chartText(cv, font, 8f, LBL_NO_DATA, CHART_AXIS,
                px + pw / 2f - 50, py + ph / 2f - 4);
        }

        // X axis tick labels
        for (int i = 0; i <= xDiv; i++) {
            double dataX = maxX * i / (double) xDiv;
            float cx = px + pw * i / (float) xDiv;
            String lbl = FMT_INT.format(dataX);
            float tw = font.getWidth(lbl, 7f);
            chartText(cv, font, 7f, lbl, CHART_AXIS, cx - tw / 2, py - 13);
        }

        // Y axis tick labels
        for (int i = 0; i <= yDiv; i++) {
            double dataY = maxY * i / (double) yDiv;
            float cy = py + ph * i / (float) yDiv;
            String lbl = FMT_INT.format(dataY);
            float tw = font.getWidth(lbl, 7f);
            chartText(cv, font, 7f, lbl, CHART_AXIS, px - tw - 4, cy - 3);
        }

        // Axis title
        chartText(cv, font, 7f, "RPM", CHART_AXIS, px + pw / 2f - 7, py - 26);

        // Legend (top-left inside plot area)
        float legX = px + 6;
        float legY = py + ph - 8;
        for (int i = 0; i < series.size(); i++) {
            ChartSeriesModel s = series.get(i);
            cv.saveState();
            cv.setFillColor(hexRgb(s.getColorHex()));
            cv.rectangle(legX, legY - 5, 8, 6);
            cv.fill();
            cv.restoreState();
            chartText(cv, font, 7f, s.getLabel(), CHART_AXIS, legX + 11, legY - 5);
            legY -= 11;
        }
    }

    private static void chartText(
        PdfCanvas cv,
        PdfFont font,
        float size,
        String text,
        DeviceRgb color,
        float x,
        float y
    ) {
        cv.saveState();
        cv.setFillColor(color);
        cv.beginText()
            .setFontAndSize(font, size)
            .moveText(x, y)
            .showText(text)
            .endText();
        cv.restoreState();
    }


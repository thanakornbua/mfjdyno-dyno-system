    private static Document openDoc(PdfDocument pdfDoc, PdfFont font) {
        Document doc = new Document(pdfDoc, PageSize.A4);
        doc.setMargins(48, 48, 48, 48);
        doc.setFont(font);
        doc.setFontSize(10);
        return doc;
    }

    /** A4 content width = 595 - 2*48 = 499 pt */
    private static float contentWidth() {
        return 499f;
    }

    // =========================================================================
    // Header / footer
    // =========================================================================

    private static void addHeader(
        Document doc,
        String title,
        String subtitle,
        PdfFont font
    ) {
        Image logo = loadLogoImage();
        if (logo != null) {
            doc.add(logo
                .scaleToFit(96f, 58f)
                .setHorizontalAlignment(HorizontalAlignment.CENTER)
                .setMarginBottom(6));
        }

        doc.add(new Paragraph(title)
            .setFont(font).setFontSize(17).setBold()
            .setFontColor(COL_TXT_MAIN)
            .setTextAlignment(TextAlignment.CENTER)
            .setMarginBottom(4));

        if (subtitle != null && !subtitle.isEmpty()) {
            doc.add(new Paragraph(subtitle)
                .setFont(font).setFontSize(11)
                .setFontColor(COL_ACCENT)
                .setTextAlignment(TextAlignment.CENTER)
                .setMarginBottom(2));
        }

        doc.add(new Paragraph(LBL_FOOTER + "  •  " + now())
            .setFont(font).setFontSize(8)
            .setFontColor(COL_TXT_MUTED)
            .setTextAlignment(TextAlignment.CENTER)
            .setMarginBottom(10));

        // Accent divider
        doc.add(accentDivider(font));
    }

    private static Image loadLogoImage() {
        try (InputStream in = DynoPdfExporter.class.getResourceAsStream(LOGO_RESOURCE)) {
            if (in == null) {
                return null;
            }
            return new Image(ImageDataFactory.create(in.readAllBytes()));
        } catch (Exception ignored) {
            return null;
        }
    }

    private static void addFooter(Document doc, PdfFont font) {
        doc.add(new Table(UnitValue.createPercentArray(new float[]{1}))
            .setWidth(UnitValue.createPercentValue(100))
            .setBorder(Border.NO_BORDER)
            .setMarginTop(8)
            .addCell(new Cell()
                .setBorder(Border.NO_BORDER)
                .setBorderTop(new SolidBorder(COL_BORDER, 0.5f))
                .setPadding(4)
                .add(new Paragraph(LBL_FOOTER)
                    .setFont(font).setFontSize(8)
                    .setFontColor(COL_TXT_MUTED)
                    .setTextAlignment(TextAlignment.CENTER))));
    }

    private static Table accentDivider(PdfFont font) {
        return new Table(UnitValue.createPercentArray(new float[]{1}))
            .setWidth(UnitValue.createPercentValue(100))
            .setBorder(Border.NO_BORDER)
            .setMarginBottom(12)
            .addCell(new Cell()
                .setBorder(Border.NO_BORDER)
                .setBorderBottom(new SolidBorder(COL_ACCENT, 2f))
                .setPadding(0)
                .add(new Paragraph("").setFontSize(1)));
    }


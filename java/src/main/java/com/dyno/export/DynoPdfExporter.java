    private static List<ChartSeriesModel> makeSingleRunSeries(
        Long runId,
        List<RunHistoryFrameDto> frames,
        int colorIndex
    ) {
        List<ChartPlotPoint> pwrPts = new ArrayList<ChartPlotPoint>();
        List<ChartPlotPoint> tqPts  = new ArrayList<ChartPlotPoint>();
        for (int i = 0; i < frames.size(); i++) {
            RunHistoryFrameDto f = frames.get(i);
            Double rpm = f.getEngineRpm();
            if (rpm == null || rpm.doubleValue() <= 0) continue;
            if (f.getPowerHp() != null && f.getPowerHp().doubleValue() >= 0) {
                pwrPts.add(new ChartPlotPoint(rpm.doubleValue(), f.getPowerHp().doubleValue()));
            }
            if (f.getTorqueNm() != null && f.getTorqueNm().doubleValue() >= 0) {
                tqPts.add(new ChartPlotPoint(rpm.doubleValue(), f.getTorqueNm().doubleValue()));
            }
        }
        int ci = colorIndex % POWER_HEX.length;
        String label = runId != null ? String.format("RUN-%05d", runId.longValue()) : "RUN";
        List<ChartSeriesModel> out = new ArrayList<ChartSeriesModel>(2);
        if (!pwrPts.isEmpty()) {
            out.add(new ChartSeriesModel("pw-" + runId, label + " Power",  POWER_HEX[ci],  pwrPts));
        }
        if (!tqPts.isEmpty()) {
            out.add(new ChartSeriesModel("tq-" + runId, label + " Torque", TORQUE_HEX[ci], tqPts));
        }
        return out;
    }

    private static List<ChartSeriesModel> makeCompareSeries(List<ComparedRunDto> runs) {
        List<ChartSeriesModel> all = new ArrayList<ChartSeriesModel>();
        for (int i = 0; i < runs.size(); i++) {
            ComparedRunDto cr = runs.get(i);
            if (cr.getRun() == null) continue;
            List<RunHistoryFrameDto> frames = cr.getFrames() != null
                ? cr.getFrames() : Collections.<RunHistoryFrameDto>emptyList();
            all.addAll(makeSingleRunSeries(cr.getRun().getRunId(), frames, i));
        }
        return all;
    }


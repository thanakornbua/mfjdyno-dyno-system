    private static final class SweepStats {
        private final MinMax powerPs;
        private final MinMax torqueNm;

        private SweepStats(MinMax powerPs, MinMax torqueNm) {
            this.powerPs = powerPs;
            this.torqueNm = torqueNm;
        }

        private static SweepStats fromFrames(List<RunHistoryFrameDto> frames) {
            MinMax power = new MinMax();
            MinMax torque = new MinMax();
            if (frames != null) {
                for (int i = 0; i < frames.size(); i++) {
                    RunHistoryFrameDto frame = frames.get(i);
                    if (frame == null) {
                        continue;
                    }
                    power.accept(ps(frame.getPowerHp()));
                    torque.accept(frame.getTorqueNm());
                }
            }
            return new SweepStats(power, torque);
        }
    }

    private static final class MinMax {
        private Double min;
        private Double max;
        private int count;

        private void accept(Double value) {
            if (value == null || !Double.isFinite(value.doubleValue())) {
                return;
            }
            min = min == null ? value : Double.valueOf(Math.min(min.doubleValue(), value.doubleValue()));
            max = max == null ? value : Double.valueOf(Math.max(max.doubleValue(), value.doubleValue()));
            count += 1;
        }

        private boolean isMax(Double value) {
            return value != null && max != null && close(value.doubleValue(), max.doubleValue());
        }

        private boolean isMin(Double value) {
            return count > 1 && value != null && min != null && !close(min.doubleValue(), max.doubleValue())
                && close(value.doubleValue(), min.doubleValue());
        }

        private static boolean close(double a, double b) {
            return Math.abs(a - b) < 0.0001d;
        }
    }
